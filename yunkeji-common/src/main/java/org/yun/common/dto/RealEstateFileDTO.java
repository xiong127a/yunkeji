package org.yun.common.dto;

public class RealEstateFileDTO {
    
    private Long id;
    
    private Long queryRecordId;
    
    private String fileType;
    
    private String fileName;
    
    private String filePath;
    
    private Long fileSize;
    
    private String createdAt;
    
    // Getters and setters
    public Long getId() {
        return id;
    }
    
    public void setId(Long id) {
        this.id = id;
    }
    
    public Long getQueryRecordId() {
        return queryRecordId;
    }
    
    public void setQueryRecordId(Long queryRecordId) {
        this.queryRecordId = queryRecordId;
    }
    
    public String getFileType() {
        return fileType;
    }
    
    public void setFileType(String fileType) {
        this.fileType = fileType;
    }
    
    public String getFileName() {
        return fileName;
    }
    
    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
    
    public String getFilePath() {
        return filePath;
    }
    
    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }
    
    public Long getFileSize() {
        return fileSize;
    }
    
    public void setFileSize(Long fileSize) {
        this.fileSize = fileSize;
    }
    
    public String getCreatedAt() {
        return createdAt;
    }
    
    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }
}